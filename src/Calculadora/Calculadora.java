package Calculadora;

public class Calculadora {
    private int numero1;
    private int numero2;

    private int sumaNumeros(int numero1, int numero2){
        return numero1+numero2;
    }
    private int restaNumeros(int numero1, int numero2){
        return numero1-numero2;
    }
    private int multiplicacionNumeros(int numero1, int numero2){
        return numero1*numero2;
    }
    private int divisionNumeros(int numero1, int numero2){
        return  numero1/numero2;
    }
}
